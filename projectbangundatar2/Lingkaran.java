public class Lingkaran {
    //Hitung Luas
    double hitungLuasLingkaran(int r){
        return(r * r * 22/7);
    }
    double hitungLuasLingkaran(double r){
        return(r * r * 22/7);
    }
    
    //Hitung Keliling
    double hitungKelilingLingkaran(int r){
        return (2 * r * 22/7);
    }
    double hitungKelilingLingkaran(double r){
        return (2 * r * 22/7);
    }
}
